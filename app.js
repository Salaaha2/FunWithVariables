// Declare ten different variables
var a = 5;
var b = 10;
var c = 15;
var d = 20;
var e = 25;
var f = 30;
var g = 35;
var h = 40;
var i = 45;
var j = 50;

// 2x console.log() addition of two variables
console.log("a + g = ", a + g);
console.log("c + j = ", c + j);

// 2x console.log() subtraction of two variables
console.log("f - e = ", f - e);
console.log("b - h = ", b - h);

// 2x console.log() multiplication of two variables
console.log("h * d = ", h * d);
console.log("i * f = ", i * f);

// 2x console.log() division of two variables
console.log("e / c = ", e / c);
console.log("g / h = ", g / h);

// 2x console.log() incerments of two variables
console.log("f * 2 ", f += 2);
console.log("j * 2 ", j += 2);

// 2x console.log() decrement of two variables
console.log("a - 2 ", a -= 2);
console.log("b - 2 ", b -= 2);